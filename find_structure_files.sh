#!/bin/bash
# ========================================================================
# 查找structure.dat文件位置的脚本
# ========================================================================

echo "========================================================================="
echo "查找structure.dat文件位置"
echo "========================================================================="

BASE_PATH="/home/task3/xrqliang/solps/SOLPS-ITER-3.0.7-20201205/runs/EAST"

echo "在EAST目录及其子目录中查找structure.dat文件..."
echo ""

# 查找所有structure.dat文件
find "$BASE_PATH" -name "structure.dat" -type f 2>/dev/null | while read -r file; do
    echo "找到: $file"
    echo "  大小: $(ls -lh "$file" | awk '{print $5}')"
    echo "  修改时间: $(ls -l "$file" | awk '{print $6, $7, $8}')"
    echo ""
done

echo "========================================================================="
echo "查找完成"
echo "========================================================================="
